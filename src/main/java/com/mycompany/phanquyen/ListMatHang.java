    private void FillMatHang() {
        productService = new ProductService();
        List<MatHang> productList = productService.getAll();
        DefaultTableModel model = (DefaultTableModel) TableProduct.getModel();
        model.setRowCount(0);
        for (MatHang mathang : productList) {
            Object[] row = {false, mathang.getMALOAI(), mathang.getMAMH(), mathang.getTENMH(), mathang.getGIABAN(), mathang.getDVT(), mathang.getMOTA()};
            model.addRow(row);
        }
    }

    private void FillCmbLoaiHang() {
        productService = new ProductService();
        List<MatHang> productList = productService.getAll();
        List<String> LoaiMatHang = new ArrayList<>();
        for (var item : productList) {
            if (!LoaiMatHang.contains(item.getMALOAI())) {
                LoaiMatHang.add(item.getMALOAI());
            }
        }
        for (var item : LoaiMatHang) {
            cmb_loaiMatHang.addItem(item);
        }
    }

    private void FillDonViTinh() {
        productService = new ProductService();
        List<MatHang> productList = productService.getAll();
        List<String> donViTinh = new ArrayList<>();
        for (var item : productList) {
            if (!donViTinh.contains(item.getDVT())) {
                donViTinh.add(item.getDVT());
            }
        }
        for (var item : donViTinh) {
            cmb_dvt.addItem(item);
        }
    }

