    private void TableProductMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableProductMouseClicked
        int row = TableProduct.getSelectedRow();
        TableProduct.setValueAt(!(Boolean) TableProduct.getValueAt(row, 0), row, 0);
        cmb_loaiMatHang.setSelectedItem(TableProduct.getValueAt(row, 1));
        txt_maMatHang.setText(TableProduct.getValueAt(row, 2).toString());
        txt_matHang.setText(TableProduct.getValueAt(row, 3).toString());
        txt_price.setText(TableProduct.getValueAt(row, 4).toString());
        cmb_dvt.setSelectedItem(TableProduct.getValueAt(row, 5));
        jTextArea_desciption.setText(TableProduct.getValueAt(row, 6).toString());
        currentId = TableProduct.getValueAt(row, 2).toString();
    }//GEN-LAST:event_TableProductMouseClicked

    private void addSelectionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addSelectionMouseClicked
        MatHang product = new MatHang();
        product.setMAMH(txt_maMatHang.getText());
        product.setTENMH(txt_matHang.getText());
        product.setGIABAN(Float.parseFloat(txt_price.getText()));
        product.setDVT(cmb_dvt.getSelectedItem().toString());
        product.setMALOAI(cmb_loaiMatHang.getSelectedItem().toString());
        product.setMOTA(jTextArea_desciption.getText());
        product.setVOHIEUHOA("false");
        productService.addProduct(product);
        FillMatHang();
    }//GEN-LAST:event_addSelectionMouseClicked

    private void editSelectionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editSelectionMouseClicked
        try {
            MatHang product = new MatHang();
            product.setMAMH(txt_maMatHang.getText());
            product.setTENMH(txt_matHang.getText());
            product.setGIABAN(Float.parseFloat(txt_price.getText()));
            product.setDVT(cmb_dvt.getSelectedItem().toString());
            product.setMALOAI(cmb_loaiMatHang.getSelectedItem().toString());
            product.setMOTA(jTextArea_desciption.getText());
            product.setVOHIEUHOA("false");
            productService.updateProduct(product, currentId);
            FillMatHang();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Sửa thất bại!!! ", "failed",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_editSelectionMouseClicked

    private void delSelectionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_delSelectionMouseClicked
        if (productService.getProductById(txt_maMatHang.getText()) != null) {
            productService.deleteProduct(txt_maMatHang.getText());
            FillMatHang();
        } else {
            JOptionPane.showMessageDialog(null, "Xóa thất bại!!! ", "failed",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_delSelectionMouseClicked

